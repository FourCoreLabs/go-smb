package smb

const (
	SMB1CommandNegotiate byte = 0x72
)

// MS-CIFS 2.2.3.1 SMB Header
type SMB1Header struct { // 32 bytes
	Protocol         []byte `smb:"fixed:4"` // Must contain 0xff, S, M, B
	Command          uint8
	Status           uint32
	Flags            uint8
	Flags2           uint16
	PIDHigh          uint16
	SecurityFeatures []byte `smb:"fixed:8"`
	Reserved         uint16
	TID              uint16
	PIDLow           uint16
	UID              uint16
	MID              uint16
}
